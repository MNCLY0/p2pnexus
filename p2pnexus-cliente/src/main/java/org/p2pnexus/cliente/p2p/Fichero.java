package org.p2pnexus.cliente.p2p;

public class Fichero {
    String nombre;
    String ruta;
    String size;
    String extension;

    public Fichero(String nombre, String ruta, String size, String extension) {
        this.nombre = nombre;
        this.ruta = ruta;
        this.size = size;
        this.extension = extension;
    }

}

